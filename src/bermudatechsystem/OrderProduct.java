    public void getProductToBeOrdered()
    {
        String stockingProductID;
        stockingProductID = productIDToBeOrdered.getText();
         
         for(Product e : Database.companyDatabase.products)
         {
             if(stockingProductID.equalsIgnoreCase(e.getProductID()))
             {
                 String stockingHardwareType = e.getHardwareType();
                 stockingProductName = e.getProductName();
                 String stockingProductPrice = Integer.toString(e.getPrice());
                 String stockingCurrentProductQuantity = Integer.toString(e.getQuantity());
                 stockingSupplierID = e.getSupplier();
                 
                 hardwareTypeToBeOrdered.setText(stockingHardwareType);
                 productNameToBeOrdered.setText(stockingProductName);
                 productPriceToBeOrdered.setText(stockingProductPrice);
                 currentProductQuantityToBeOrdered.setText(stockingCurrentProductQuantity);
                 addedSupplierID.setText(stockingSupplierID);
              }
         }
         for(Supplier f: Database.companyDatabase.suppliers)
         {
             if(stockingSupplierID.equalsIgnoreCase(f.getSupplierID()))
             {
                 String writingSupplierName = f.getSupplierName();
                 String writingSupplierRepresentativeName = f.getRepresentativeName();
                 String writingSupplierRepresentativeNumber = f.getRepresentativeNumber();
                 String writingSupplierRepresentativeEmail = f.getRepresentativeEmail();
                 String writingSuppliedProducts = f.getSuppliedProduct();
                 
                 supplierID.setText(stockingSupplierID);
                 supplierName.setText(writingSupplierName);
                 supplierRepresentativeName.setText(writingSupplierRepresentativeName);
                 supplierRepresentativeNumber.setText(writingSupplierRepresentativeNumber);
                 supplierRepresentativeEmail.setText(writingSupplierRepresentativeEmail);
                 suppliedProducts.setText(writingSuppliedProducts);
             }
         }
    }        
    
