    private void btnOrderProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderProductActionPerformed
       int confirmResponse = JOptionPane.showConfirmDialog(this,"Do you want to confirm this order?", "Confirm Order", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
       
       if(confirmResponse == JOptionPane.YES_OPTION)
       {
            String amountOrdered = quantityToBeOrdered.getText();
            String purchaseOrder = purchaseOrderNumber.getText();
            
            String displayMessage = ( amountOrdered +" "+ stockingProductName + " have been Ordered \n" + "Purchase Order Number for this order is: " + purchaseOrder );
            
            JOptionPane.showMessageDialog(null, displayMessage, " Product Ordered ", JOptionPane.INFORMATION_MESSAGE);
       }
       else
       {
           JOptionPane.showMessageDialog(null, "Order Has Not Been Placed", "Order Cancelled", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_btnOrderProductActionPerformed

